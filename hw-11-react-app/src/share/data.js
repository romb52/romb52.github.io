export const result = (shop) => {
  const data = shop.reduce(
    (acc, curr) => {
      return {
        count: acc.count + curr.count,
        sum: acc.sum + curr.price * curr.count,
      };
    },
    { count: 0, sum: 0 }
  );

  return {
    count: data.count,
    avg: (data.sum / data.count).toFixed(2),
    sum: data.sum.toFixed(2),
  };
};

export const listPerPage = 6;

export const pag = (dataList) => {
  const arr = [];
  const countPages = Math.ceil(dataList.length / listPerPage);
  for (let i = 1; i <= countPages; i++) {
    arr.push(i);
  }
  return arr;
};

export const sortData = (isAZ, name, search) => {
  const { newData, pagList } = countResult(search);
  // const newData = [...countResult(search).newData];
  if (name === 'Name') {
    newData.sort((a, b) => {
      return isAZ ? a.name.localeCompare(b.name) : b.name.localeCompare(a.name);
    });
  }

  if (name === 'Articule') {
    newData.sort((a, b) => {
      return isAZ
        ? a.articule.localeCompare(b.articule)
        : b.articule.localeCompare(a.articule);
    });
  }

  if (name === 'Count') {
    newData.sort((a, b) => {
      return isAZ ? a.count - b.count : b.count - a.count;
    });
  }

  if (name === 'AVG Price') {
    newData.sort((a, b) => {
      return isAZ ? a.avg - b.avg : b.avg - a.avg;
    });
  }

  if (name === 'Sum') {
    newData.sort((a, b) => {
      return isAZ ? a.sum - b.sum : b.sum - a.sum;
    });
  }

  return { newData, pagList };
};
const filterData = (search) => {
  const lc = search.toLowerCase();
  return data.filter((item) => {
    if (
      item.name.toLowerCase().indexOf(lc) !== -1 ||
      item.articule.toLowerCase().indexOf(lc) !== -1
    ) {
      return true;
    }

    if (item.shop) {
      const matchingShops = item.shop.filter(
        (el) => el.shop_name.toLowerCase().indexOf(lc) !== -1
      );

      if (matchingShops.length > 0) {
        return true;
      }
    }

    return false;
  });
};
export const countResult = (search) => {
  const arr = search === '' ? [...data] : [...filterData(search)];
  const newData = arr.map((item) => {
    const { count, avg, sum } = result(item.shop);
    return {
      name: item.name,
      articule: item.articule,
      shop: item.shop,
      count,
      avg,
      sum,
    };
  });
  const pagList = pag(newData);
  return { newData, pagList };
};
